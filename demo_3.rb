"hello" =~ /e\w{2}/ # 1: соответствует символу e, сопровождаемому двумя словарными символами

# после любого успешного (не nil) соответствия глобальная переменная $~ содержит объект MatchData,
# который, в свою очередь содержит полную информацию о соответствии

p $~.string # hello: строка целиком
p $~.to_s   # ell: соответствующая часть строки
p $~.pre_match # h: часть до соответствия
p $~.post_match # o: часть после соответстсвия